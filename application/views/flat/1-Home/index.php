			<h1>Why?</h1>
			<p>The authors &amp; editors of first available printed works in Samskrita on various subjects were all traditional pandits who had absolute mastery over their subjects. To bring their works back into circulation via the digital medium is one of the biggest motivations here. The second and third generation books might lack the rigor and authenticity of the early works and if we could make available the old gems of the 19th and early 20th century, that would come as a breath of fresh air as far as traditional literature in Samskrita is concerned.</p>
		</div>
	</div>
